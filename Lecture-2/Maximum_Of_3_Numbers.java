public class Maximum_Of_3_Numbers {
    public static void main(String[] args) {
        int a = 8;
        int b = 7;
        int c = 6;
        if (a > b && a > c) {
            System.out.println("a is the greatest");
        } if (b > a && b > c) {
            System.out.println("b is the greatest");
        } if (c > a && c > b) {
            System.out.println("c is the greatest");
        }
    }
}

